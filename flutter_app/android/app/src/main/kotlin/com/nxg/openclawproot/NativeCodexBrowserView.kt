            "canGoBack" to (active?.canGoBack ?: false),
            "canGoForward" to (active?.canGoForward ?: false),
            "tabs" to tabs.mapIndexed { index, tab ->
                refreshTabNavigationState(tab)
                linkedMapOf<String, Any?>(
