    private fun createActionButton(iconRes: Int, description: String, onClick: () -> Unit): FrameLayout {
        return FrameLayout(context).apply {
            contentDescription = description
            isClickable = true
            isFocusable = true
            isHapticFeedbackEnabled = true
            background = controlStateDrawable(
                normalColor = NativeUiPalette.accentSoft,
                pressedColor = NativeUiPalette.accentPressed,
                strokeColor = NativeUiPalette.accent,
            )
            addView(
                ImageView(context).apply {
                    setImageDrawable(tintedIcon(iconRes))
                    this.contentDescription = description
                },
                LayoutParams(dp(16), dp(16), Gravity.CENTER),
            )
            setOnClickListener {
                performHapticFeedback(HapticFeedbackConstants.KEYBOARD_TAP)
                onClick()
            }
        }.also { button ->
            button.layoutParams = LinearLayout.LayoutParams(
                dp(32),
                dp(30),
            ).apply {
                marginEnd = dp(4)
            }
        }
    }

    private fun configureIconButton(
        target: FrameLayout,
        iconRes: Int,
        description: String,
        rotation: Float = 0f,
        onClick: (View) -> Unit,
    ) {
        target.removeAllViews()
        target.background = controlStateDrawable(NativeUiPalette.surfaceRaised, NativeUiPalette.borderStrong)
        target.contentDescription = description
        target.isClickable = true
        target.isFocusable = true
        target.isHapticFeedbackEnabled = true
        target.setOnClickListener {
            target.performHapticFeedback(HapticFeedbackConstants.KEYBOARD_TAP)
            onClick(it)
        }
        val iconDrawable = tintedIcon(iconRes)
        target.addView(
            ImageView(context).apply {
                setImageDrawable(iconDrawable)
                this.rotation = rotation
                contentDescription = description
            },
            LayoutParams(dp(16), dp(16), Gravity.CENTER),
        )
        target.layoutParams = LinearLayout.LayoutParams(dp(30), dp(30)).apply {
            marginEnd = dp(4)
        }
    }

    private fun setCenteredButtonIcon(target: FrameLayout, iconRes: Int, description: String? = null) {
        target.removeAllViews()
        description?.let { target.contentDescription = it }
        target.addView(
            ImageView(context).apply {
                setImageDrawable(tintedIcon(iconRes))
                contentDescription = description ?: target.contentDescription
            },
            FrameLayout.LayoutParams(dp(16), dp(16), Gravity.CENTER),
        )
    }

    private fun createSmallActionButton(
        label: String,
        active: Boolean = false,
        iconOnly: Boolean = true,
        onClick: () -> Unit,
    ): View {
        val iconRes = smallActionIcon(label)
        if (iconOnly && iconRes != null) {
            return FrameLayout(context).apply {
                contentDescription = label
                isClickable = true
                isFocusable = true
                isHapticFeedbackEnabled = true
