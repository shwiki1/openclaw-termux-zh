    private fun runStoredScript(
        script: NativeBrowserStoredScript,
        variables: Map<String, String>,
    ) {
        val logView = TextView(context).apply {
            setTextColor(Color.WHITE)
            textSize = 11f
            typeface = Typeface.MONOSPACE
            setPadding(dp(18), dp(14), dp(18), dp(14))
            text = "准备运行 ${script.fileName} …"
        }
        val dialog = showScriptWorkbenchDialog(
            iconRes = R.drawable.lucide_play,
            title = "运行自动化脚本",
            subtitle = script.fileName,
            content = ScrollView(context).apply {
                addView(
                    logView,
                    ViewGroup.LayoutParams(
                        ViewGroup.LayoutParams.MATCH_PARENT,
                        ViewGroup.LayoutParams.WRAP_CONTENT,
                    ),
                )
            },
            heightRatio = 0.58f,
        ) { currentDialog ->
            addView(createScriptDialogButton(R.drawable.lucide_x, "关闭") { currentDialog.dismiss() })
        }
        val logLines = mutableListOf<String>()
        fun appendLog(line: String) {
            logLines += line
            logView.text = logLines.joinToString("\n")
        }
        fun finishRun(success: Boolean, message: String) {
            appendLog(message)
            if (success) {
                persistStoredScriptRun(script.id)
            }
        }
        fun runStep(index: Int) {
            if (index >= script.steps.size) {
                finishRun(true, "全部 ${script.steps.size} 步已完成。")
                return
            }
            val step = script.steps[index]
            val action = normalizeStoredScriptAction(step.action)
            if (action !in RUNNABLE_SCRIPT_ACTIONS) {
                finishRun(false, "第 ${index + 1} 步不支持运行: ${step.action}")
                return
            }
            val resolvedPayload = step.payload.resolveScriptVariables(variables)
            appendLog("步骤 ${index + 1}/${script.steps.size} · $action")
            executeAction(action, resolvedPayload) { result ->
                val ok = result["ok"] != false
                val message = result["message"]?.toString()?.trim().orEmpty()
                    .ifEmpty { if (ok) "已完成。" else "执行失败。" }
                appendLog("${if (ok) "成功" else "失败"} · $message")
                if (!ok) {
                    finishRun(false, "脚本在第 ${index + 1} 步停止。")
                    return@executeAction
                }
                runStep(index + 1)
            }
        }
        runStep(0)
    }

    private fun listScriptLibrary(payload: Map<String, Any?>): Map<String, Any?> {
        val filter = payload.stringValue("filter").lowercase()
        val scripts = loadStoredAutomationScripts().filter { script ->
            if (filter.isEmpty()) {
                true
            } else {
                listOf(script.id, script.fileName, script.description, script.sourceUrl, script.sourceTitle)
                    .joinToString(" ")
                    .lowercase()
                    .contains(filter)
            }
        }
        return snapshot(
            message = if (scripts.isEmpty()) "No browser scripts matched the request." else "Browser scripts loaded.",
            extra = mapOf(
                "count" to scripts.size,
                "scripts" to scripts.map { it.toJsonMap(includeCommand = true) },
                "pendingDraft" to pendingScriptDraft?.toJsonMap(),
            ),
        )
    }

    private fun stageScriptDraft(payload: Map<String, Any?>): Map<String, Any?> {
        val explicitSteps = scriptStepsFromPayload(payload["steps"])
        val steps = if (explicitSteps.isNotEmpty()) {
            explicitSteps
        } else {
            recentRecordableSteps.takeLast(payload.intValue("maxRecentSteps", 16).coerceIn(1, 48))
        }
        if (steps.isEmpty()) {
            return snapshot(
                ok = false,
                message = "No repeatable browser actions are available to stage. Provide explicit steps or run repeatable browser actions first.",
            )
        }
        val activeTab = activeTabOrNull()
        val sourceUrl = payload.stringValue("sourceUrl", activeTab?.currentUrl.orEmpty())
        val sourceTitle = payload.stringValue("sourceTitle", activeTab?.title.orEmpty())
        val draft = NativeBrowserPendingScriptDraft(
            fileName = normalizeStoredScriptFileName(
                payload.stringValue("fileName", defaultScriptFileName()),
            ),
            description = payload.stringValue(
                "description",
                defaultScriptDescription(steps, sourceTitle, sourceUrl),
            ),
            steps = steps,
            variables = stringListFromAny(payload["variables"]),
            sourceUrl = sourceUrl,
            sourceTitle = sourceTitle,
            updatedAt = java.time.Instant.now().toString(),
            autoGenerated = payload.booleanValue("autoGenerated"),
        )
        pendingScriptDraft = draft
        return snapshot(
            message = "Browser script draft staged: ${draft.fileName}",
            extra = mapOf("pendingDraft" to draft.toJsonMap()),
        )
    }

    private fun clearPendingScriptDraft(): Map<String, Any?> {
        val hadDraft = pendingScriptDraft != null
        pendingScriptDraft = null
        return snapshot(
            message = if (hadDraft) "Pending browser script draft cleared." else "There was no pending browser script draft.",
            extra = mapOf("pendingDraft" to null),
        )
    }

    private fun saveScriptFromPayload(payload: Map<String, Any?>): Map<String, Any?> {
        val explicitSteps = scriptStepsFromPayload(payload["steps"])
        val draft = pendingScriptDraft
        val steps = when {
            explicitSteps.isNotEmpty() -> explicitSteps
            payload.booleanValue("useRecent") || draft == null -> recentRecordableSteps.takeLast(payload.intValue("maxRecentSteps", 16).coerceIn(1, 48))
            else -> draft.steps
        }
        if (steps.isEmpty()) {
            return snapshot(ok = false, message = "No repeatable browser actions are available to save.")
        }
        val activeTab = activeTabOrNull()
        val fileName = payload.stringValue("fileName", draft?.fileName ?: defaultScriptFileName())
        val description = payload.stringValue(
            "description",
            draft?.description ?: defaultScriptDescription(steps, activeTab?.title.orEmpty(), activeTab?.currentUrl.orEmpty()),
        )
        saveStoredAutomationScript(
            fileName = fileName,
            description = description,
            steps = steps,
            variables = stringListFromAny(payload["variables"]).ifEmpty { draft?.variables ?: emptyList() },
            sourceUrl = payload.stringValue("sourceUrl", draft?.sourceUrl ?: activeTab?.currentUrl.orEmpty()),
            sourceTitle = payload.stringValue("sourceTitle", draft?.sourceTitle ?: activeTab?.title.orEmpty()),
        )
        if (draft != null && explicitSteps.isEmpty() && !payload.booleanValue("useRecent")) {
            pendingScriptDraft = null
        }
        val saved = loadStoredAutomationScripts().firstOrNull()
        return snapshot(
            message = "Browser script saved: ${saved?.fileName ?: normalizeStoredScriptFileName(fileName)}",
            extra = mapOf(
                "script" to saved?.toJsonMap(includeCommand = true),
                "pendingDraft" to pendingScriptDraft?.toJsonMap(),
            ),
        )
    }

    private fun runSavedScriptFromPayload(
        payload: Map<String, Any?>,
        callback: (Map<String, Any?>) -> Unit,
    ) {
        val id = payload.stringValue("id")
        val fileName = payload.stringValue("fileName")
        val script = loadStoredAutomationScripts().firstOrNull { stored ->
            (id.isNotEmpty() && stored.id == id) ||
                (fileName.isNotEmpty() && stored.fileName == normalizeStoredScriptFileName(fileName))
        }
        if (script == null) {
            callback(snapshot(ok = false, message = "Browser script not found."))
            return
        }
        val variables = when (val rawVariables = payload["variables"]) {
            is Map<*, *> -> rawVariables.entries.mapNotNull { (key, value) ->
                val stringKey = key as? String ?: return@mapNotNull null
                stringKey to value?.toString().orEmpty()
            }.toMap()
            is JSONObject -> rawVariables.toMap().mapValues { it.value?.toString().orEmpty() }
            else -> emptyMap()
        }
        runStoredScript(script, variables)
        callback(
            snapshot(
                message = "Browser script started: ${script.fileName}",
                extra = mapOf("script" to script.toJsonMap(includeCommand = true)),
            ),
        )
    }

    private fun renameScriptFromPayload(payload: Map<String, Any?>): Map<String, Any?> {
        val id = payload.stringValue("id")
        val scripts = loadStoredAutomationScripts().toMutableList()
        val index = scripts.indexOfFirst { it.id == id }
        if (id.isEmpty() || index < 0) {
            return snapshot(ok = false, message = "Browser script not found.")
        }
        scripts[index] = scripts[index].copy(
            fileName = normalizeStoredScriptFileName(payload.stringValue("fileName", scripts[index].fileName)),
            description = payload.stringValue("description", scripts[index].description),
            updatedAt = java.time.Instant.now().toString(),
        )
        writeStoredAutomationScripts(scripts)
        return snapshot(message = "Browser script renamed: ${scripts[index].fileName}")
    }

    private fun deleteScriptFromPayload(payload: Map<String, Any?>): Map<String, Any?> {
        val id = payload.stringValue("id")
        if (id.isEmpty()) {
            return snapshot(ok = false, message = "Provide id to delete a browser script.")
        }
        val before = loadStoredAutomationScripts()
        val after = before.filterNot { it.id == id }
        writeStoredAutomationScripts(after)
        val deleted = after.size != before.size
        return snapshot(
            ok = deleted,
            message = if (deleted) "Browser script deleted." else "Browser script not found.",
        )
    }

