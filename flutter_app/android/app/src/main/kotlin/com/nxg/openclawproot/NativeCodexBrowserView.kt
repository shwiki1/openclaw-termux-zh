    private fun formatStoredDate(value: String): String? {
        val normalized = value.trim()
        if (normalized.isEmpty()) {
            return null
        }
        val instant = runCatching { java.time.Instant.parse(normalized) }.getOrNull() ?: return normalized
        val local = java.time.ZonedDateTime.ofInstant(instant, java.time.ZoneId.systemDefault())
        return "%04d-%02d-%02d %02d:%02d".format(
            local.year,
            local.monthValue,
            local.dayOfMonth,
            local.hour,
            local.minute,
        )
    }

    private fun normalizeStoredScriptFileName(value: String): String {
        var normalized = value.trim()
        if (normalized.isEmpty()) {
            normalized = "browser-script"
        }
        normalized = normalized
            .replace(Regex("[\\\\/:*?\"<>|\\r\\n]+"), "_")
            .replace(Regex("\\s+"), "-")
        if (normalized.length > 80) {
            normalized = normalized.substring(0, 80)
        }
        if (!normalized.endsWith(".json")) {
            normalized = "$normalized.browser.json"
        }
        return normalized
    }

    private fun defaultScriptFileName(): String {
        val now = java.time.ZonedDateTime.now()
