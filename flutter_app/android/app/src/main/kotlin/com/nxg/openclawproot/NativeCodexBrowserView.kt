    private fun scriptStepsFromPayload(rawSteps: Any?): List<NativeBrowserStoredScriptStep> {
        val items = when (rawSteps) {
            is List<*> -> rawSteps
            is JSONArray -> rawSteps.toList()
            else -> return emptyList()
        }
        return items.mapNotNull { item ->
            val map = when (item) {
                is Map<*, *> -> item.entries.mapNotNull { (key, value) ->
                    val stringKey = key as? String ?: return@mapNotNull null
                    stringKey to value
                }.toMap()
                is JSONObject -> item.toMap()
                else -> return@mapNotNull null
            }
            val action = normalizeStoredScriptAction(map["action"]?.toString().orEmpty())
            if (action.isEmpty() || action !in RUNNABLE_SCRIPT_ACTIONS) {
                return@mapNotNull null
            }
            val payload = when (val rawPayload = map["payload"]) {
                is Map<*, *> -> rawPayload.entries.mapNotNull { (key, value) ->
                    val stringKey = key as? String ?: return@mapNotNull null
                    stringKey to value
                }.toMap()
                is JSONObject -> rawPayload.toMap()
                else -> emptyMap()
            }
            NativeBrowserStoredScriptStep(
                action = action,
                payload = payload,
                note = map["note"]?.toString()?.trim().orEmpty(),
            )
        }
    }

    private fun stringListFromAny(raw: Any?): List<String> = when (raw) {
        is List<*> -> raw.mapNotNull { it?.toString()?.trim()?.takeIf(String::isNotEmpty) }
        is JSONArray -> raw.toStringList()
        is String -> raw.split(Regex("\\r?\\n|,")).mapNotNull { it.trim().takeIf(String::isNotEmpty) }
        else -> emptyList()
    }.distinct()

